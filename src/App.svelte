<script>
  import { onMount, beforeUpdate } from 'svelte';
  import { App } from 'konsta/src/svelte';
  import { Router, Route, createHistory } from 'svelte-navigator';
  import createHashSource from './hashHistory.js';

  import routes from './routes.js';
  import HomePage from './pages/Home.svelte';

  let theme = 'ios';
  const setTheme = (t) => {
    theme = t;
  };
  let currentColorTheme = '';
  const setColorTheme = (color) => {
    const htmlEl = document.documentElement;
    htmlEl.classList.forEach((c) => {
      if (c.includes('k-color')) htmlEl.classList.remove(c);
    });
    if (color) htmlEl.classList.add(color);
    currentColorTheme = color;
  };

  window.setTheme = setTheme;
  window.setMode = (mode) => {
    if (mode === 'dark') document.documentElement.classList.add('dark');
    else document.documentElement.classList.remove('dark');
  };

  const inIFrame = window.parent !== window;

  const calcSafeAreas = () => {
    if (window.location.href.includes('safe-areas')) {
      const html = document.documentElement;
      if (html) {
        html.style.setProperty(
          '--k-safe-area-top',
          theme === 'ios' ? '44px' : '24px'
        );
        html.style.setProperty('--k-safe-area-bottom', '34px');
      }
    }
  };

  onMount(() => {
    calcSafeAreas();
  });

  beforeUpdate(() => {
    calcSafeAreas();
  });

  const hash = createHistory(createHashSource());
</script>

<App {theme} safeAreas={!inIFrame}>
  <Router history={hash} primary={false}>
    <Route path="/">
      <HomePage
        {theme}
        {setTheme}
        colorTheme={currentColorTheme}
        {setColorTheme}
      />
    </Route>
    {#each routes as route}
      <Route path={route.path} component={route.component} />
    {/each}
  </Router>
</App>
